#include <iostream>
using namespace std;
int main(){
    //bitwise and
    cout<< (4 & 5) << endl;

    //bitwise or
    cout<< (4|5) << endl;

    //bitwise not
    cout<< (~5)<< endl
    ;

    //bitwise left shift
    cout<< (5<<1) << endl;

    //bitwise right shift
    cout<< (1>>1) << endl;

    //bitwise XOR
    cout<< (4^5);


    return 0;
} 